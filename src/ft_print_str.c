#include "ft_printf.h"

char	ft_print_str(char *str)
{
	int	i;

	i = 0;
	if (str[i] != NULL)
	{
		while (str[i] != '\0')
		{
			write(1, &str[i], 1);
			i++;
		}
	}
}
